using System;
using System.Collections.Generic;
using System.Drawing;
using System.Text;
using ReportFormDesign.Model;

namespace ReportFormDesign.DataModels
{
    public class AutoSortDataModel : DataModel
    {
        public AutoSortDataModel()
        {
        }

        public AutoSortDataModel(string mainText, float mainData, float MaxData, Color color)
            : base(mainText, mainData)
        {
            this.MaxData = MaxData;
            this.ModelColor = color;
        }


        public float MaxData { get; set; }
    }
}
